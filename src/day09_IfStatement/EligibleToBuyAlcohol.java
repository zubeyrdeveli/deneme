package day09_IfStatement;

public class EligibleToBuyAlcohol {
    public static void main(String[] args) {

        String name = "John";
        int age = 17;

        if (age >= 18) {
            System.out.println(name + " is eligible to buy alcohol." );}

        else {
            System.out.println(name + " is NOT eligible to buy alcohol.");
        }

    }
}
