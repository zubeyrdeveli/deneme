package day07_Operators;

public class UnaryOperators {
    public static void main(String[] args) {

        int num1 = 25;
        int num2 = -25;

        System.out.println(num1 < 0);
        System.out.println(num2 < 0);

        System.out.println("---------------------------");

        int z = 45;
        System.out.println(++z);
        System.out.println(z++);
        System.out.println(z);
        System.out.println("------------------------");
        int q = 30;
        System.out.println(--q);
        System.out.println(q--);
        System.out.println(q);


    }



}
