package day07_Operators;

public class RelationalOperators {

    public static void main(String[] args) {

        boolean result1 = 200 > 40;

        System.out.println("result1 = " + result1);

        boolean result2 = 20 >= 40;

        System.out.println("result2 = " + result2);

        boolean result3 = 33 >= 33;

        System.out.println("result3 = " + result3);

        boolean result4 = 23 < 20;

        System.out.println("result4 = " + result4);

        boolean result5 = 45 <= 55;

        System.out.println("result5 = " + result5);

        System.out.println("-----------------------");

        int x = 100;
        int y = 200;

        boolean equal = x!=y;

        System.out.println("equal = " + equal);

        boolean metin = "Remzi" == "Remzi";

        System.out.println("metin = " + metin);

        boolean result6 = 33==33;

        System.out.println("result6 = " + result6);

        boolean result7 = 50 != 50;

        System.out.println("result7 = " + result7);

        boolean result8 = 44 != 43;

        System.out.println("result8 = " + result8);

        boolean result9 = "Java" != "Break";

        System.out.println("result9 = " + result9);


    }



}
