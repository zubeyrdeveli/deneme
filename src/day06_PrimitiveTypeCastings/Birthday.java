package day06_PrimitiveTypeCastings;

public class Birthday {
    public static void main(String[] args) {

        String name = "Zubeyr";
        int birthDay = 5;
        String birthMonth = "April";
        int birthYear = 1988;

        System.out.println(name + " was born on " + birthMonth + "/" + birthDay + "/" + birthYear );



    }


}
